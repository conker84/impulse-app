_HISTOGRAM_CLASS_MAP = {
    HistogramType.DURATION: "HistogramDuration",
    HistogramType.DISTANCE: "HistogramDistance",
    HistogramType.DURATION_COUNT: "HistogramDurationCount",
    HistogramType.EVENT_COUNT: "HistogramEventCount",
}


def generate_histogram_page(state: ReportState) -> str:
    lines = [
        "# Databricks notebook source",
        "# MAGIC %md",
        "# MAGIC ##### Histogram Visualizations",
        "# MAGIC",
        "# MAGIC Auto-generated by Impulse.",
        "",
        "# COMMAND ----------",
        "",
        "my_first_page = Page(page_number=1)",
        "my_first_chapter.add_page(my_first_page)",
        "",
    ]

    for hist in state.histograms:
        lines.append("# COMMAND ----------")
        lines.append("")

        cls_name = _HISTOGRAM_CLASS_MAP[hist.histogram_type]
        lines.append(f"# MAGIC %md")
        lines.append(f"# MAGIC ##### {hist.description or hist.name}")
        lines.append("")
        lines.append("# COMMAND ----------")
        lines.append("")

        params = [
            f'    name="{hist.name}"',
            f'    base_expr=signals["{hist.signal_ref}"]',
            f"    bins={hist.bins}",
        ]

        if hist.histogram_type == HistogramType.DURATION and hist.max_duration:
            params.append(f"    max_duration={hist.max_duration}")
        elif hist.histogram_type == HistogramType.EVENT_COUNT and hist.event_signal_ref:
            params.append(f'    event_expr=signals["{hist.event_signal_ref}"]')
        elif hist.histogram_type == HistogramType.DISTANCE and hist.weight_signal_ref:
            params.append(f'    weights_expr=signals["{hist.weight_signal_ref}"]')

        if hist.histogram_type in (HistogramType.DURATION_COUNT, HistogramType.EVENT_COUNT):
            params.append(f"    weight_const={hist.weight_const or 1.0}")

        if hist.description:
            params.append(f'    desc="{hist.description}"')
        if hist.bins_unit:
            params.append(f'    bins_unit="{hist.bins_unit}"')
        if hist.values_unit:
            params.append(f'    values_unit="{hist.values_unit}"')

        lines.append(f"hist = {cls_name}(")
        lines.append(",\n".join(params) + ",")
        lines.append(")")
        lines.append("my_first_page.add_visualization(hist)")
        lines.append("")

    return "\n".join(lines)


