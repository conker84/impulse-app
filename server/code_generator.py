_DEFAULT_SESSION_METRIC_COLUMNS = [
    "measurement_first_datapoint_timestamp",
    "measurement_last_datapoint_timestamp",
    "measurement_session_start_odo_mileage",
    "measurement_session_end_odo_mileage",
    "test_object_id",
]


def generate_config_json(state: ReportState) -> dict[str, Any]:
    ds = state.data_sources

    vehicles = []
    for v in state.vehicles:
        entry: dict[str, Any] = {
            "vehicle_id": {"col_name": v.col_name, "col_type": v.col_type, "value": v.vehicle_id},
            "start_ts": {
                "col_name": "measurement_first_datapoint_timestamp",
                "col_type": "timestamp",
                "value": v.start_ts,
            },
        }
        if v.stop_ts:
            entry["stop_ts"] = {
                "col_name": "measurement_last_datapoint_timestamp",
                "col_type": "timestamp",
                "value": v.stop_ts,
            }
        vehicles.append(entry)

    source: dict[str, Any] = {
        "container_metrics": ds.container_metrics,
        "channel_metrics": ds.channel_metrics,
        "channels": ds.channels if len(ds.channels) > 1 else ds.channels[0] if ds.channels else "",
    }
    if ds.aliases:
        source["aliases"] = ds.aliases
    if ds.aliases_copy_table_name:
        source["aliases_copy_table_name"] = ds.aliases_copy_table_name
    if ds.device_aliases:
        source["device_aliases"] = ds.device_aliases
    if ds.device_aliases_copy_table_name:
        source["device_aliases_copy_table_name"] = ds.device_aliases_copy_table_name

    return {
        "metadata": {
            "report_name": f"{state.name} Report",
            "description": state.description,
            "report_creator": "Impulse",
            "source_code": "",
        },
        "data": {
            "source": source,
            "destination": {
                "catalog": ds.destination_catalog,
                "schema": ds.destination_schema,
                "table_prefix": ds.table_prefix or f"{state.name}_report",
            },
            "query_solver": {
                "name": "narrow_solver",
                "rle_data": "true",
                "config": "DEFAULT_V10",
                "max_sessions_per_run": 10000,
                "max_batch_size": 10000,
            },
        },
        "vehicles": vehicles,
        "session_metric_table": {
            "columns": _DEFAULT_SESSION_METRIC_COLUMNS,
            "checkpoint_col_name": "measurement_first_datapoint_timestamp",
            "col_name_mappings": [
                {
                    "existing_col_name": "measurement_first_datapoint_timestamp",
                    "new_col_name": "first_datapoint_timestamp",
                    "casting_type": "timestamp",
                },
                {
                    "existing_col_name": "measurement_last_datapoint_timestamp",
                    "new_col_name": "last_datapoint_timestamp",
                    "casting_type": "timestamp",
                },
            ],
        },
    }


def generate_all_files(state: ReportState) -> dict[str, str]:
    """Return a dict mapping relative file paths to their content."""
    return {
        "src/01_signal_definitions.py": generate_signal_definitions(state),
        "src/chapters/01_chapter/pages/01_histograms.py": generate_histogram_page(state),
        "src/chapters/01_chapter/orchestrator.py": generate_orchestrator(state),
        "src/config/dev_config.json": json.dumps(generate_config_json(state), indent=2),
    }
