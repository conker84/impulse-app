# 01_signal_definitions.py  (define-channels skill step 5)
# ---------------------------------------------------------------------------

def generate_signal_definitions(state: ReportState) -> str:
    lines = [
        "# Databricks notebook source",
        "# MAGIC %md",
        "# MAGIC ##### Signal Definitions",
        "# MAGIC",
        "# MAGIC Auto-generated by Impulse.",
        "",
        "# COMMAND ----------",
        "",
    ]

    physical = [s for s in state.signals if s.signal_type == "physical"]
    virtual = [s for s in state.signals if s.signal_type == "virtual"]

    if physical:
        lines.append("# Physical channels")
        for sig in physical:
            desc = f"  # {sig.description}" if sig.description else ""
            # Use channel_name for direct silver layer lookup;
            # fall back to alias for legacy alias-based setups
            ch_name = sig.channel_name or sig.alias or sig.var_name
            lines.append(
                f'{sig.var_name} = query.channel('
                f'channel_name="{ch_name}"){desc}'
            )
        lines.append("")
        lines.append("# COMMAND ----------")
        lines.append("")

    if virtual:
        lines.append("# Virtual / derived signals")
        for sig in virtual:
            desc = f"  # {sig.description}" if sig.description else ""
            lines.append(f"{sig.var_name} = {sig.expression}{desc}")
        lines.append("")
        lines.append("# COMMAND ----------")
        lines.append("")

    lines.append("# Signals dictionary for access by visualizations")
    lines.append("signals = {")
    for sig in state.signals:
        lines.append(f'    "{sig.var_name}": {sig.var_name},')
    lines.append("}")
    lines.append("")
    lines.append("# Make all variables available in the global scope")
    lines.append("globals().update(locals())")

    return "\n".join(lines)


