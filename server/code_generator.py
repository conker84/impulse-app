def generate_orchestrator(state: ReportState) -> str:
    chapter_name = state.description or f"{state.name} Chapter"
    return "\n".join([
        "# Databricks notebook source",
        f'my_first_chapter = Chapter(name="{chapter_name}", description="Auto-generated chapter")',
        "my_report.add_chapter(my_first_chapter)",
        "",
        '# COMMAND ----------',
        "",
        '%run "./pages/01_histograms"',
    ])


